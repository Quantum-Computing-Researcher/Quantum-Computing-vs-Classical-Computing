#The function returns the edge list, couplings, fields, planted spins, and the analytic optimal energy Eâ˜… = âˆ’(#edges) âˆ’ field_strengthÂ·N.
def make_planted_ising(N: int, p: float, field_strength: float, seed: int):
    """
    ER(N,p) graph; planted sâ˜… âˆˆ {Â±1}; set J_ij = sâ˜…_i sâ˜…_j on edges; h_i = field_strength * sâ˜…_i.
    Then E(sâ˜…) = -(#edges) - field_strength * N  (unique optimum if field_strength>0).
    """
    rng = np.random.default_rng(seed)
    s_star = rng.choice([-1, 1], size=N).astype(np.int8)
    edges = []
    for i in range(N):
        for j in range(i+1, N):
            if rng.random() < p:
                edges.append((i, j))
    m = len(edges)
    J = {(i, j): int(s_star[i]) * int(s_star[j]) for (i, j) in edges}
    h = field_strength * s_star.astype(np.float64)
    E_star = -m - field_strength * N
    return edges, J, h, s_star, E_star

#def energy_of computes the Ising Hamiltonian energy by summing all pairwise interaction terms over edges and subtracting the linear field contribution.
#It evaluates âˆ’âˆ‘(i,j) J_ij s_i s_j âˆ’ hÂ·s for a given spin configuration and returns the total energy as a float.

def energy_of(spins: np.ndarray, edges, J, h) -> float:
    e = 0.0
    for (i, j) in edges:
        e -= J[(i, j)] * spins[i] * spins[j]
    e -= np.dot(h, spins)
    return float(e)


#def build_neighbors constructs an adjacency list mapping each spin to its neighbors and couplings, enabling efficient local energy updates.
def build_neighbors(N, edges, J):
    nbrs_by_i = {i: [] for i in range(N)}
    for (i, j) in edges:
        Jij = J[(i, j)]
        nbrs_by_i[i].append((j, Jij))
        nbrs_by_i[j].append((i, Jij))
    return nbrs_by_i

def mean_field_rhs(m: np.ndarray, s: float, Gamma: float, nbrs_by_i, h: np.ndarray) -> np.ndarray:
    """
    Compute RHS dm/dt for all spins at schedule point s.
    m shape: (N,3) with rows [mx, my, mz] per spin; |m_i|â‰ˆ1 ideally.
    """
    N = m.shape[0]
    Bx = (1.0 - s) * Gamma
    z_field = np.zeros(N, dtype=np.float64)
    # z_field[i] = sum_j J_ij * m[j,2]
    for i in range(N):
        acc = 0.0
        for j, Jij in nbrs_by_i[i]:
            acc += Jij * m[j, 2]
        z_field[i] = s * (acc + h[i])

    dm = np.zeros_like(m)
    # dm_i = 2 * m_i Ã— B_i ;  B_i = (Bx, 0, z_field[i])
    # Cross product:
    # mx,my,mz crossed with (Bx,0,Bz)
    # = ( my*Bz - mz*0, mz*Bx - mx*Bz, mx*0 - my*Bx )
    dm[:, 0] = 2.0 * (m[:, 1] * z_field)                   # dmx
    dm[:, 1] = 2.0 * (m[:, 2] * Bx - m[:, 0] * z_field)    # dmy
    dm[:, 2] = -2.0 * (m[:, 1] * Bx)                       # dmz
    return dm

#def renorm_rows normalizes each row vector of the array m to unit length in-place.
#It safeguards against division by zero by replacing zero norms with one before normalization.
def renorm_rows(m: np.ndarray):
    norms = np.linalg.norm(m, axis=1, keepdims=True)
    norms[norms == 0] = 1.0
    m[:] = m / norms

def evolve_mean_field_QA(N: int, steps: int, Gamma: float, nbrs_by_i, h: np.ndarray, seed: int):
    """
    Heun (RK2) integrator over s âˆˆ [0,1]; t discretized into `steps`.
    Init state = +x for all spins with tiny seed-dependent perturbation to break ties.
    Returns final magnetizations m (N,3).
    """
    rng = np.random.default_rng(seed)
    m = np.zeros((N, 3), dtype=np.float64)
    m[:, 0] = 1.0  # +x ground state at s=0
    # tiny coherent perturbations (noiseless but breaks symmetry deterministically per seed)
    eps = 1e-3
    m[:, 1] += eps * rng.standard_normal(N)
    m[:, 2] += eps * rng.standard_normal(N)
    renorm_rows(m)

    dt = 1.0 / max(1, steps)
    s = 0.0
    for k in range(steps):
        s_k = s
        s_k1 = min(1.0, s + dt)
        k1 = mean_field_rhs(m, s_k,  Gamma, nbrs_by_i, h)
        m_pred = m + dt * k1
        renorm_rows(m_pred)
        k2 = mean_field_rhs(m_pred, s_k1, Gamma, nbrs_by_i, h)
        m += 0.5 * dt * (k1 + k2)
        renorm_rows(m)
        s = s_k1
    return m

def _run_single_experiment(N: int, seed: int) -> dict:
    """
    Core experiment logic - run single quantum annealing experiment.
    """
    # Fixed parameters matching the command line requirements
    steps = 1000
    p = 0.2
    field_strength = 0.1
    Gamma = 1.0
    gap_tol = 0.01

    edges, J, h, s_star, E_star = make_planted_ising(N, p, field_strength, seed)
    nbrs_by_i = build_neighbors(N, edges, J)

    tracemalloc.start()
    t0 = time.perf_counter()

    m = evolve_mean_field_QA(N, steps, Gamma, nbrs_by_i, h, seed+1)
    # Readout: round by sign of m_z
    spins = np.sign(m[:, 2]).astype(np.int8)
    spins[spins == 0] = 1  # tie -> +1
    best_E = energy_of(spins, edges, J, h)

    runtime = time.perf_counter() - t0
    _, peak = tracemalloc.get_traced_memory()
    tracemalloc.stop()

    gap = (best_E - E_star) / (abs(E_star) + 1e-12)
    success = bool(gap <= gap_tol)

    # Cleanup GPU memory
    safe_gpu_cleanup()

    return {
        "n": N,
        "edges": len(edges),
        "p": p,
        "steps": steps,
        "Gamma": Gamma,
        "field_strength": field_strength,
        "seed": seed,
        "best_energy": float(best_E),
        "opt_energy": float(E_star),
        "gap": float(gap),
        "correct": bool(success),
        "runtime_s": float(runtime),
        "peak_mem_mb": float(peak / (1024**2)),
        "device": get_device_name()
    }

