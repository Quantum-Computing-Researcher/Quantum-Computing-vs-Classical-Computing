#This def make_signal_2tone function generates a synthetic one- or two-tone real signal with additive white Gaussian noise.
#It dynamically selects between CuPy and NumPy arrays to support GPU or CPU execution transparently.
#Signal phases and frequencies are randomized, while noise power is scaled to achieve a target SNR.
#Backend-specific random number generation is used to ensure compatibility and performance on each device.

def make_signal_2tone(N: int, rng, snr_db: float = 30.0):
    """Length-N real 2-tone signal with integer bins in [1, N//2-1] + AWGN."""
    if N < 8:
        k1 = rng.integers(1, max(2, N//2))
        true_bins = [int(k1)]
    else:
        k1 = rng.integers(1, N//2)
        k2 = rng.integers(1, N//2 - 1)
        if k2 >= k1: k2 += 1
        true_bins = sorted([int(k1), int(k2)])

    # Use the appropriate array library (CuPy or NumPy)
    n = cp.arange(N, dtype=cp.float64)
    ph1 = rng.uniform(0, 2*cp.pi)
    ph2 = rng.uniform(0, 2*cp.pi)
    x = cp.cos(2*cp.pi*k1*n/N + ph1)
    if len(true_bins) == 2:
        x = x + cp.cos(2*cp.pi*k2*n/N + ph2)

    sig_pow = cp.mean(x**2)
    snr_lin = 10.0**(snr_db/10.0)
    noise_pow = sig_pow / snr_lin

    # Generate noise using the appropriate RNG
    if HAS_GPU:
        # Use CuPy's random for GPU
        noise = cp.random.normal(0.0, cp.sqrt(noise_pow), size=N)
    else:
        # Use NumPy's random for CPU
        noise = rng.normal(0.0, math.sqrt(noise_pow), size=N)
        noise = cp.asarray(noise)

    x = x + noise
    return x, true_bins

#This def topk_one_sided_bins helper extracts the strongest frequency bins from a one-sided spectrum using the active compute backend.
#It computes magnitudes, optionally suppresses the DC component, and ranks bins by descending strength.
#CuPy operations are used for efficiency, with results converted back to host integers for compatibility.

def topk_one_sided_bins(X: cp.ndarray, k: int):
    N = X.shape[0]
    one = X[:N//2+1]
    mag = cp.abs(one)
    if k >= 2 and len(mag) > 0:
        mag = mag.copy(); mag[0] = 0.0  # suppress DC when looking for tones
    idx = cp.argsort(-mag)[:k]
    return sorted(int(i) for i in cp.asnumpy(idx))

#This function evaluates how accurately dominant frequency bins are recovered from a spectral estimate.
#It compares estimated peak locations against known true bins within an optional tolerance.
#The result is returned as a normalized recovery fraction to remain robust for small or degenerate cases.

def accuracy_recovery(X: cp.ndarray, true_bins, tol_bins: int = 0):
    k = len(true_bins)
    est = topk_one_sided_bins(X, k)
    matched = sum(1 for t in true_bins if any(abs(t - e) <= tol_bins for e in est))
    return matched / max(1, k)

#These routines support error measurement and transformation construction for complex-valued signals.
#The RMSE function computes magnitude error while handling GPU and CPU backends appropriately.
def rmse_complex(a: cp.ndarray, b: cp.ndarray) -> float:
    diff = a - b
    if HAS_GPU:
        return float(cp.sqrt(cp.mean((diff.real**2 + diff.imag**2))))
    else:
        return float(np.sqrt(np.mean((cp.asnumpy(diff.real)**2 + cp.asnumpy(diff.imag)**2))))

#The QFT builder creates a fully dense, normalized transform matrix using the active array library.
def qft_dense_matrix(N: int) -> cp.ndarray:
    n = cp.arange(N).reshape(1, N)
    k = cp.arange(N).reshape(N, 1)
    return cp.exp(2j * cp.pi * (k @ n) / N) / math.sqrt(N)

def _run_single_experiment(N: int, steps: int, acc_tol: float, seed: int) -> dict:
    # Use numpy RNG for consistency, but convert all operations to GPU arrays
    rng_np = np.random.default_rng(seed)

    tracemalloc.start()
    t0 = time.perf_counter()

    try:
        # Precompute QFT matrix once for this N
        F = qft_dense_matrix(N)

        accs, rmses = [], []

        for _ in range(steps):
            # Generate signal directly on GPU
            x, true_bins = make_signal_2tone(N, rng_np, snr_db=30.0)

            # Normalize to unit L2 norm
            norm = cp.linalg.norm(x)
            if norm == 0.0:
                x_amp = cp.ones(N, dtype=cp.complex128) / math.sqrt(N)
            else:
                x_amp = (x / norm).astype(cp.complex128)

            # Apply QFT using dense matrix multiplication
            state = F @ x_amp

            # Reference spectrum (matches QFT definition)
            # Use appropriate FFT implementation
            if HAS_GPU:
                # Use CuPy's FFT
                X_ref = cp.fft.ifft(x_amp) * math.sqrt(N)
            else:
                # Use NumPy's FFT
                x_amp_cpu = cp.asnumpy(x_amp)
                X_ref_cpu = np.fft.ifft(x_amp_cpu) * math.sqrt(N)
                X_ref = cp.asarray(X_ref_cpu)

            accs.append(accuracy_recovery(state, true_bins, tol_bins=0))
            rmses.append(rmse_complex(state, X_ref))

        runtime = time.perf_counter() - t0
        _, peak = tracemalloc.get_traced_memory()
        tracemalloc.stop()

        acc_mean = float(cp.mean(cp.asarray(accs))) if accs else 0.0
        rmse_mean = float(cp.mean(cp.asarray(rmses))) if rmses else 0.0

        success = bool(acc_mean >= acc_tol)

    except Exception as e:
        print(f"âŒ Experiment failed for N={N}, seed={seed}: {e}")
        runtime = time.perf_counter() - t0
        _, peak = tracemalloc.get_traced_memory()
        tracemalloc.stop()
        acc_mean = 0.0
        rmse_mean = 0.0
        success = False

    # Cleanup GPU memory
    safe_gpu_cleanup()

    return {
        "N": int(N),
        "steps": int(steps),
        "seed": int(seed),
        "accuracy_mean": acc_mean,
        "rmse_vs_fft": rmse_mean,
        "success": success,
        "runtime_s": float(runtime),
        "peak_mem_mb": float(peak / (1024**2)),
        "signals_processed": int(steps),
        "device": get_device_name()
    }
