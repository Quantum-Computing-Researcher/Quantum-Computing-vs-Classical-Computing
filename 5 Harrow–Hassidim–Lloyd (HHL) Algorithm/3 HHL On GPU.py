# ------------********------------ Ideal HHL Core (GPU-accelerated)
# The following function:
#Implements an idealized, noiseless version of HHL for diagonal positive-definite matrices by directly applying Aâ»Â¹ to |bâŸ©.
#Uses GPU acceleration (CuPy) when available and safeguards against division by extremely small eigenvalues.
#Returns the unnormalized classical vector Aâ»Â¹b, which corresponds to the exact solution in this simplified diagonal-HHL setting.

def hhl_ideal_solve_diagonal(A_diag, b):
    """
    Ideal noiseless HHL effect for diagonal Hermitian positive-definite A:
      |x> âˆ A^{-1} |b>
    GPU-accelerated version using CuPy/NumPy.
    Returns the (unnormalized) classical vector x_pred = A^{-1} b,
    which matches the exact solution in this idealized setting.
    """
    # Guard against tiny eigenvalues to avoid division issues
    eps = 1e-12
    return b / cp.maximum(A_diag, eps)


#The following function generates a diagonal SPD system A x = b, 
#  solves it with an idealized GPU-accelerated HHL routine, and measures runtime and memory.
#Converts data between CPU/GPU as needed, runs the diagonal HHL solver, 
#  and computes relative error against the known ground-truth solution.
#Returns a full benchmark record including size, seed, runtime, peak memory, accuracy, and device information.

def run_experiment(N: int, seed: int) -> dict:
    """
    Generate a Hermitian positive-definite diagonal A of size N,
    a ground-truth x_true, set b := A x_true, and recover x via ideal HHL.
    Measure runtime, peak memory, and relative error vs x_true.
    GPU-accelerated version.
    """
    rng = np.random.default_rng(seed)

    # Construct diagonal SPD matrix: diag entries approx in [5,6)
    A_diag_np = 5.0 + rng.random(N)
    x_true_np = rng.random(N)
    b_np = A_diag_np * x_true_np

    # Convert to GPU arrays if available
    if HAS_GPU:
        A_diag = cp.asarray(A_diag_np)
        b = cp.asarray(b_np)
    else:
        A_diag = A_diag_np
        b = b_np

    # Measure time + peak memory for the HHL pipeline
    tracemalloc.start()
    t0 = time.perf_counter()

    # --- Ideal HHL solve (noiseless) ---
    x_pred = hhl_ideal_solve_diagonal(A_diag, b)

    # Convert back to CPU for error calculation
    if HAS_GPU:
        x_pred_np = cp.asnumpy(x_pred)
    else:
        x_pred_np = x_pred

    runtime = time.perf_counter() - t0
    _, peak = tracemalloc.get_traced_memory()
    tracemalloc.stop()

    # Cleanup GPU memory
    safe_gpu_cleanup()

    # Accuracy: same metric as classical baseline
    rel_error = np.linalg.norm(x_pred_np - x_true_np) / np.linalg.norm(x_true_np)

    return {
        "N": N,
        "seed": seed,
        "method": "hhl_ideal_diagonal",
        "runtime_s": float(runtime),
        "peak_mem_mb": float(peak / (1024 ** 2)),
        "rel_error": float(rel_error),
        "device": get_device_name()
    }

