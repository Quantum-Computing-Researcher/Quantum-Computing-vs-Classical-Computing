def prepare_zero_state(n: int):
    """Prepare |0^n> state. GPU-accelerated."""
    if HAS_GPU:
        st = cp.zeros(1 << n, dtype=cp.complex128)
    else:
        st = np.zeros(1 << n, dtype=np.complex128)
