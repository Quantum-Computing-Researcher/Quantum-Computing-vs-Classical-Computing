#def hadamard_on_qubit applies a Hadamard gate to a selected qubit 
#   with support for GPU and CPU execution paths.
#It checks whether the state resides on the GPU and 
#   chooses the appropriate computation method.
#It updates the quantum state in place by 
#   combining amplitude pairs using normalized sums.

def hadamard_on_qubit(state, q: int, n: int):
    """Apply H on qubit q (little-endian bit ordering). GPU-accelerated."""
    dim = state.shape[0]
    m = 1 << q          # block size
    stride = m << 1
    inv_sqrt2 = 1.0 / math.sqrt(2.0)

    if HAS_GPU and isinstance(state, cp.ndarray):
        # GPU vectorized version
        for base in range(0, dim, stride):
            i0 = base
            i1 = base + m
            for off in range(m):
                a = state[i0 + off]
                b = state[i1 + off]
                state[i0 + off] = (a + b) * inv_sqrt2
                state[i1 + off] = (a - b) * inv_sqrt2
    else:
        # CPU version
        for base in range(0, dim, stride):
            i0 = base
            i1 = base + m
            for off in range(m):
                a = state[i0 + off]
                b = state[i1 + off]
                state[i0 + off] = (a + b) * inv_sqrt2
                state[i1 + off] = (a - b) * inv_sqrt2


#This function def z_on_qubit () applies a Z gate to a specified qubit with support for both GPU and CPU arrays.
#It creates an index mask to identify amplitudes where the target qubit is set.
#It flips the sign of those amplitudes in place using the appropriate backend.

def z_on_qubit(state, q: int):
    """Apply Z on qubit q: multiply amplitudes with bit(q)==1 by -1. GPU-accelerated."""
    dim = state.shape[0]
    bit = 1 << q

    if HAS_GPU and isinstance(state, cp.ndarray):
        # GPU vectorized version
        idx = cp.arange(dim, dtype=cp.uint64)
        mask = (idx & bit) != 0
        state[mask] = -state[mask]
    else:
        # CPU version
        idx = np.arange(dim, dtype=np.uint64)
        mask = (idx & bit) != 0
        state[mask] = -state[mask]



