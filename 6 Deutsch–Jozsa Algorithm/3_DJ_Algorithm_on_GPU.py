#This function  def oracle_constant_phase () creates a phase oracle for a constant Boolean function.
#It returns an inner function that applies a global phase flip when c equals one.
#It models constant Deutschâ€“Jozsa oracles via uniform state sign inversion.
def oracle_constant_phase(c: int):
    """
    Phase oracle for constant f(x)=c:
      c=0 -> identity; c=1 -> global phase (-1)
    """
    def apply(state, n: int):
        if c & 1:
            state *= -1.0
    return apply


#The function oracle_parity_phase defines a corrected balanced parity phase oracle 
# for the Deutsch Jozsa algorithm.
#It computes the parity sÂ·x for each basis state and applies a corresponding phase flip.
#It supports both GPU and CPU execution paths using vectorized operations.

def oracle_parity_phase(s_bits: np.ndarray):
    """
    CORRECTED: Balanced parity oracle for f(x) = sÂ·x mod 2
    Applies phase (-1)^(sÂ·x) to each basis state |xâŸ©
    """
    s = s_bits.astype(np.uint8)
    def apply(state, n: int):
        dim = state.shape[0]
        # Apply phase based on parity: (-1)^(sÂ·x)
        if HAS_GPU and isinstance(state, cp.ndarray):
            # GPU vectorized version
            indices = cp.arange(dim, dtype=cp.uint64)
            # Compute sÂ·x for each x
            parity = cp.zeros(dim, dtype=cp.uint8)
            for i in range(n):
                if s[i]:
                    parity ^= ((indices >> i) & 1).astype(cp.uint8)
            # Apply phase: multiply by (-1)^parity
            phase = 1 - 2 * parity.astype(cp.float64)
            state *= phase.astype(cp.complex128)
        else:
            # CPU version
            indices = np.arange(dim, dtype=np.uint64)
            parity = np.zeros(dim, dtype=np.uint8)
            for i in range(n):
                if s[i]:
                    parity ^= ((indices >> i) & 1).astype(np.uint8)
            phase = 1 - 2 * parity.astype(np.float64)
            state *= phase.astype(np.complex128)
    return apply

