#This def gf2_rank () function computes the rank of a binary matrix over GF(2) using Gaussian elimination.
#It ensures compatibility by converting GPU arrays to NumPy arrays before processing.
#It returns the number of linearly independent rows found during elimination.

def gf2_rank(A: np.ndarray) -> int:
    """Return rank of binary matrix A over GF(2)."""
    # Ensure we're working with NumPy arrays for linear algebra
    if HAS_GPU and isinstance(A, cp.ndarray):
        A = cp.asnumpy(A)

    A = (A.astype(np.uint8) & 1).copy()
    m, n = A.shape
    row = 0
    for col in range(n):
        # find pivot
        pivot = None
        for r in range(row, m):
            if A[r, col]:
                pivot = r; break
        if pivot is None:
            continue
        if pivot != row:
            A[[row, pivot]] = A[[pivot, row]]
        # eliminate in all other rows
        for r in range(m):
            if r != row and A[r, col]:
                A[r, :] ^= A[row, :]
        row += 1
        if row == m:
            break
    return row

#def gf2_solve_homogeneous_one () function solves a homogeneous linear system over GF(2) 
#   and returns one valid non-zero solution.
#It converts GPU arrays to NumPy for compatibility and 
#   reduces the matrix to row-reduced echelon form.
#It constructs the solution by assigning a free variable and back-substituting pivot values.

def gf2_solve_homogeneous_one(A: np.ndarray) -> np.ndarray:
    """
    Solve A s = 0 over GF(2) and return one non-zero solution (if exists).
    Assumes nullity >= 1; for Simon we expect rank = n-1 so nullity = 1.
    """
    # Ensure we're working with NumPy arrays for linear algebra
    if HAS_GPU and isinstance(A, cp.ndarray):
        A = cp.asnumpy(A)

    A = (A.astype(np.uint8) & 1).copy()
    m, n = A.shape
    row = 0
    pivots = []
    for col in range(n):
        pivot = None
        for r in range(row, m):
            if A[r, col]:
                pivot = r; break
        if pivot is None:
            continue
        if pivot != row:
            A[[row, pivot]] = A[[pivot, row]]
        for r in range(m):
            if r != row and A[r, col]:
                A[r, :] ^= A[row, :]
        pivots.append(col)
        row += 1
        if row == m:
            break

    free = [c for c in range(n) if c not in pivots]
    s = np.zeros(n, dtype=np.uint8)
    if not free:
        return s  # only zero solution
    fc = free[0]          # only one free var when rank = n-1
    s[fc] = 1
    # back-substitute pivot variables from RREF
    for r in range(len(pivots)-1, -1, -1):
        pc = pivots[r]
        val = 0
        for j in range(pc+1, n):
            if A[r, j] and s[j]:
                val ^= 1
        s[pc] = val
    return s

