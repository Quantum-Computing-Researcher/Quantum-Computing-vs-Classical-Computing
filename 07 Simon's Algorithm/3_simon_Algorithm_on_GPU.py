# def sample_y_orthogonal_to_s(n: int, s: np.ndarray, rng: np.random.Generator) -> np.ndarray: 
# It samples a binary vector that is orthogonal to a given secret vector over GF(2).
#It computes the dot product modulo two using explicit bitwise operations for compatibility.
#It repeats sampling until a valid orthogonal vector is found and returns it.

def sample_y_orthogonal_to_s(n: int, s: np.ndarray, rng: np.random.Generator) -> np.ndarray:
    """Sample y uniformly from { y ‚àà {0,1}^n : y¬∑s = 0 mod 2 }."""
    while True:
        # Generate random binary vector
        y = rng.integers(0, 2, size=n, dtype=np.uint8)

        # Compute dot product mod 2 (y¬∑s mod 2)
        # Use standard Python operations to avoid GPU-CPU compatibility issues
        dot_product = 0
        for i in range(n):
            dot_product ^= (y[i] & s[i])  # Bitwise AND then XOR for mod 2

        if dot_product == 0:
            return y

#simon_quantum_noiseless function simulates an ideal noiseless quantum Simon algorithm 
#   using classical post-processing.
#It iteratively samples vectors orthogonal to the secret until sufficient linear independence is achieved.
#It reconstructs and verifies the hidden string, 
#    returning the guess, query count, and success flag.

def simon_quantum_noiseless(n: int, s: np.ndarray, seed: int,
                            max_rounds: int | None = None):
    """
    Ideal Simon algorithm: collect random y with y¬∑s=0 until rank(Y)=n-1.
    One oracle query per round.
    """
    if max_rounds is None:
        max_rounds = 4 * n  # generous cap; expected O(n) rounds

    rng = np.random.default_rng(seed)
    Ys = []
    rank = 0
    queries = 0
    for _ in range(max_rounds):
        y = sample_y_orthogonal_to_s(n, s, rng)
        Ys.append(y)
        queries += 1

        # Convert to appropriate array type for linear algebra
        Y_matrix = np.array(Ys, dtype=np.uint8)
        rank = gf2_rank(Y_matrix)

        if rank >= n - 1:
            break

    if rank < n - 1:
        return np.zeros(n, dtype=np.uint8), queries, False

    s_guess = gf2_solve_homogeneous_one(np.array(Ys, dtype=np.uint8))

    # Compare arrays element-wise
    success = True
    for i in range(n):
        if s_guess[i] != s[i]:
            success = False
            break

    return s_guess, queries, success

def _run_single_experiment(n: int, seed: int) -> dict:
    """
    Core experiment logic - run single Simon's algorithm experiment.
    """
    rng = np.random.default_rng(seed)
    # Generate non-zero hidden string s
    s = np.zeros(n, dtype=np.uint8)
    while not s.any():
        s = rng.integers(0, 2, size=n, dtype=np.uint8)

    tracemalloc.start()
    t0 = time.perf_counter()
    s_guess, queries, success = simon_quantum_noiseless(n, s, seed=seed)
    runtime = time.perf_counter() - t0
    _, peak = tracemalloc.get_traced_memory()
    tracemalloc.stop()

    # Cleanup GPU memory
    safe_gpu_cleanup()

    return {
        "n": n,
        "seed": seed,
        "queries_used": int(queries),
        "runtime_s": float(runtime),
        "peak_mem_mb": float(peak / (1024**2)),
        "correct": bool(success),
        "device": get_device_name()
    }

