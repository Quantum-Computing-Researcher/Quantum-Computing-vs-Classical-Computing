    sizes = [6, 8, 10]
    trials = 10  # trials per size
    method = "simon"
    workers = 4  # Number of parallel threads

    # Carbon parameters
    device_power_watts = 65.0
    pue = 1.2
    combine = True

    print(f"üöÄ Starting Simon's Algorithm experiments on {get_device_name()}")
    print(f"üîß Configuration:")
    print(f"   Input sizes: {sizes}")
